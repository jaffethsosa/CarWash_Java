public class AtencionesSec {
    
    private String codigo;
    private String nombre;
    private String empleado;
    private String placa;
    private int idAtencion;
    private String estado;
    private String tipoLavado;
    private String vaciarCubiculo;

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEmpleado() {
        return empleado;
    }

    public void setEmpleado(String empleado) {
        this.empleado = empleado;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public int getIdAtencion() {
        return idAtencion;
    }

    public void setIdAtencion(int idAtencion) {
        this.idAtencion = idAtencion;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getTipoLavado() {
        return tipoLavado;
    }

    public void setTipoLavado(String tipoLavado) {
        this.tipoLavado = tipoLavado;
    }

    public String getVaciarCubiculo() {
        return vaciarCubiculo;
    }

    public void setVaciarCubiculo(String vaciarCubiculo) {
        this.vaciarCubiculo = vaciarCubiculo;
    }
    
    
}
